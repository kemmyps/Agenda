public class Contato {
    Integer id;
    String nome;
    String numero;
    String endereco;
    Boolean favorito;

    public Contato(Integer id, String nome, String numero, String endereco, Boolean favorito) {
        this.id = id;
        this.nome = nome;
        this.numero = numero;
        this.endereco = endereco;
        this.favorito = favorito;
    }
}
